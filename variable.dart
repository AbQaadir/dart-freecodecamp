void main() {
  // integer
  int age = 20;
  print(age);

  // double
  double pi = 3.14;
  print(pi);

  // string
  String name = "John Doe";
  print(name);

  // boolean
  bool isTrue = true;
  print(isTrue);

  // dynamic
  dynamic dynamicVar = 10;
  print(dynamicVar);

  dynamicVar = "Hello";
  print(dynamicVar);

  // const
  const double gravity = 9.8;
  print(gravity);

  // final
  final double piFinal = 3.14;
  print(piFinal);
}
